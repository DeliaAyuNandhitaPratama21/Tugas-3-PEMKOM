    class RefreshWeather extends SwingWorker<Object, Object> {
        private final String kota;

        public RefreshWeather(String kota) {
            this.kota = kota;
        }

        @Override
        protected Object doInBackground() throws Exception {
            ResponCuaca response = getWeatherData(kota);
            if (response != null) {
                lblKota.setText(response.getName());
                lblSuhu.setText(response.getMain().getTemp() + "Â°C");
                lblKelembaban.setText(response.getMain().getHumidity() + "%");
                lblAngin.setText(response.getWind().getSpeed() + " m/s");
                lblKondisi.setText(response.getWeather().get(0).getMain());
                }
            return null;
        }

        @Override
        protected void done() {
            System.out.println("Weather fetch completed!");
        }
    }
    
