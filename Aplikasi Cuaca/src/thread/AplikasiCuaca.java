     private ResponCuaca getWeatherData(String city) {
        String urlStr = API_URL + city + "&appid=" + API_KEY + "&units=metric";
        Gson gson = new Gson();
        StringBuilder sb = new StringBuilder();

        try {
            URL url = new URL(urlStr);
            HttpURLConnection connection = (HttpURLConnection) url.openConnection();
            try (InputStream stream = connection.getInputStream()) {
                int data = stream.read();
                while (data != -1) {
                    sb.append((char) data);
                    data = stream.read();
                }
            }
            return gson.fromJson(sb.toString(), ResponCuaca.class);
        } catch (IOException e) {
            System.out.println("Error: " + e.getMessage());
        }
        return null;
    }
     

    
    
